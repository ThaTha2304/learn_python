# If Else Statement

nama = input("Masukkan nama anda: ")

# 1. If statement ==> inline
if nama=="ThaTha": print("Wahh, kamu ThaTha :D")
print("End of Inline Statement...\n")

# 2. If statement ==> indentation
if nama == "ThaTha":
    print("Woahh, ketjee abieez!")
    print("Mantap kalii banhh~")
print("End of Indentation Statement...\n")

# If-else Statement
if nama == "ThaTha":
    print("Haii ThaTha! :D")
else:
    print("Hmmm... kamu bukan ThaTha :\"D")
print("End of If-Else statement")